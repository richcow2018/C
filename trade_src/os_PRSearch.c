/*	＜関数名＞	 long os_MBSearch(long lMpid, 													*/ 
/*								  long lAssort, TEmbcTableInd * ptEmbcInd);         			*/
/*                                	     			    										*/
/*																								*/
/*	＜機能概要＞ メイルボックス管理テーブルの検索                                               */
/*				** 種別が１の場合									                            */
/*		 		   - メイルボックス管理テーブルの個別部情報の先頭から、プロセス識別子とmpidが	*/
/*					合致する個別部を検索する                                                    */
/*						  																		*/
/*																								*/
/************************************************************************************************/
long os_PRSearch(long lMpid, long lAssort, TEmbcTableInd * ptEmbcInd)
{
	/********************************************************************************************/
	/*  ワーク領域                                                                              */
	/********************************************************************************************/	
	int lCnt2;												    // カウンター
	long lReturnMsgQ2;											// メッセージキューIDの返却値
	
	/********************************************************************************************/
	/*	 処理																				    */
	/*																							*/
	/********************************************************************************************/


	for(lCnt2 = 0; lCnt2 < ptEmbc->lIndPartNum ; lCnt2++)	// メイルボックスの個別部の先頭から
	{														// プロセス識別子とmpidを比べる
		
		if((ptEmbcInd + lCnt2)->lPCode == lMpid)		   	// プロセス識別子がmpidと合った場合は	
		{
			
			break;
		}
		
	}
		
	if(lCnt2 > ptEmbc->lIndPartNum)							// カウンターが最大個別部数を超えたという
	{														// ことはプロセス識別子がmpidと合致しない
		
		return(-1);											// -1を返却します
	}

	
	/********************************************************************************************/
	/*	 終了処理																				*/
	/*																							*/
	/********************************************************************************************/
	DbgMsg01(DLv01, (OutPut, "***** (U) BEFORE RETURN \n"));
	lReturnMsgQ2 = (ptEmbcInd + lCnt2)->lMsgQId;			// メイルボックス管理テーブル個別部情報の
															// メッセージキューIDから自分を定義した
															//「メッセージキューID」に設定する
	
	return(lReturnMsgQ2);											
	
}
