/* ＜機能概要＞ 																				*/
/*      リクエスト識別子の予約解除                                                              */
/*      予約されているリクエスト識別子を指定個数分解除する。                                    */
/*      なお、複数個の予約を取り消す場合には、対象となるリクエスト識別子は、                    */
/*      連続していなければならない。                                                            */
/*																								*/
