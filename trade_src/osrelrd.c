/* ＜仮引数＞																					*/
/*      long    rd                                                                              */
/*      long    no                                                                              */
/*																								*/
/* ＜返却値＞																					*/
/*      NORMAL      ：正常に予約を取り消した                                                    */
/*      OSEEIVNO	：個数が正の値でない、又は値が大きすぎる                                    */
/*      OSEEIVRD    ：リクエスト識別子の値が不当である                                          */
/*      OSEENRSV    ：指定範囲のリクエスト識別子のうち予約されていないものがある                */
/*																								*/
/*----------------------------------------------------------------------------------------------*/
/* ＜入出力データ構造＞ 																		*/
/* ＜制限事項＞ 																				*/
/*	   特になし 																				*/
/* ＜使用外部モジュールＩＤ＞																	*/
