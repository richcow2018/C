long osmsgqget( void )
{

	long lMyMsgQId = 0;								// メッセージキューID退避領域

	if( NULL == ptMyEMBC )							// システムテーブル管理テーブルの適正確認
	{
		DbgMsg01(DLv01, (OutPut, "@@@@@@@ ptMyEMBC == %x\n", ptMyEMBC));
		
		return(OSSEEICOD);								// 返却値暫定
	}

	lMyMsgQId = ptMyEMBC->lMsgQId;					// メッセージキューIDの取得
	
	DbgMsg01(DLv01, (OutPut, "@@@@@@@ lMyMsgQId == %ld\n", lMyMsgQId));
	
	if( 0 == lMyMsgQId )							// メッセージキューIDの適正確認
	{
		return(OSSEEICOD );
	}

	return( lMyMsgQId );

}

